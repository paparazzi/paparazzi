#ifndef LSM303DL_H
#define LSM303DL_H

/* default I2C address */
#define LSM303DL_ADDR            	0x30
#define LSM303DL_ADDR_ALT        	0x32

/* Selectable data rates in LSM303 REG1_A
 */
#define LSM303_RATE_1000 		0x3f //cut-off 780hz
#define LSM303_RATE_400 		0x37 //cut-off 292hz
#define LSM303_RATE_100  		0x2f //cuf-off 74hz
#define LSM303_RATE_50  		0x27 //cut-off 37hz


/* Registers */
#define LSM303DL_REG_CTRL_REG1_A 	0x20
#define LSM303DL_REG_CTRL_REG2_A 	0x21
#define LSM303DL_REG_CTRL_REG3_A 	0x22
#define LSM303DL_REG_CTRL_REG4_A 	0x23
#define LSM303DL_REG_CTRL_REG5_A 	0x24
#define LSM303DL_REG_HP_FILTER_RESET_A	0x25
#define LSM303DL_REG_REFERENCE_A	0x26
#define LSM303DL_REG_STATUS_REG_A	0x27
#define LSM303DL_REG_OUT_X_L_A		0x28
#define LSM303DL_REG_OUT_X_H_A		0x29
#define LSM303DL_REG_OUT_Y_L_A		0x2A
#define LSM303DL_REG_OUT_Y_H_A		0x2B
#define LSM303DL_REG_OUT_Z_L_A		0x2C
#define LSM303DL_REG_OUT_Z_H_A		0x2D
#define LSM303DL_REG_INT1_CFG_A		0x30
#define LSM303DL_REG_INT1_SRC_A		0x31
#define LSM303DL_REG_INT1_THS_A		0x32
#define LSM303DL_REG_INT1_DURATION_A	0X33
#define LSM303DL_REG_INT2_CFG_A		0X34
#define LSM303DL_REG_INT2_SRC_A		0x35
#define LSM303DL_REG_INT2_THS_A		0x36
#define LSM303DL_REG_INT2_DURATION_A	0x37

#endif /* DSM303DL_H */
